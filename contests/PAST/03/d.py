def main():
    input = stdin.readline
    n = int(input())
    num = [[] for _ in range(n)]
    for _ in range(5):
        s = list(input()[:-1])
        for i in range(n):
            si = s[4 * i + 1:4 * i + 4]
            num[i].append(si)

    ans = []
    for ni in num:
        for i in range(10):
            if NUMS[i] == ni:
                ans.append(i)

    print(*ans, sep='')


